package questao03_Ingresso;

public class Ingresso {
	
	private double valor;
	
	public Ingresso(double valor) {
		this.valor = valor;
	}
	
	public double imprimeValor() {
		return this.valor;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}
	

}
